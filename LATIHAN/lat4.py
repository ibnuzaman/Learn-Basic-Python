# Melakukan tukar nilai A,B,C,D menjadi B,D,A,C jika A,B,C,D merupakan bilangan
# desimal sembarang
aa = eval(input("Masukkan nilai a : "))
bb = eval(input("Masukkan nilai b : "))
cc = eval(input("Masukkan nilai c : "))
dd = eval(input("Masukkan nilai d : "))
# proses tukar
c = aa
a = bb
b = dd
d = cc
print("\nMelakukan tukar nilai A,B,C,D menjadi B,D,A,C jika A,B,C,D merupakan bilangan\n")
print("Setelah ditukar valuenya a :",a)
print("Setelah ditukar valuenya b :",b)
